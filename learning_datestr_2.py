#Data types- these are BIFs(Built in functions) in python

#Integer (int): represents whole numbers.
x = 10

#Float (float): Represents numbers with a decimal point.
y = 3.14

#String (str): Represents sequences of characters enclosed in quotes..
message = "Hello, World!"

#List (list): Represents an ordered collection of items.
numbers = [1, 2, 3, 4, 5]
fruits = ["apple", "banana", "orange"]

#Tuple (tuple): Represents an immutable ordered collection of items.
point = (10, 20)
dimensions = (100, 200, 300)

#Dictionary (dict): Represents an unordered collection of key-value pairs
person = {"name": "Alice", "age": 30, "city": "Auckland"}

#Boolean (bool): Represents truth values True or False.
is_valid = True
is_finished = False

